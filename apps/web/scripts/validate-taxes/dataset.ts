/**
 * Stratified 100-case dataset covering the range of the immatout calculator.
 *
 * Distribution (validated with the user):
 *   - 20 FR_NEW (new French vehicles, mix of energies/CV/regions)
 *   - 20 FR_USED (used French, various ages incl. >10yr for décote)
 *   - 20 IMPORT_EU  (neuf + occasion UE)
 *   - 20 IMPORT_NON_EU
 *   - 10 EV-focused (must be exonerated on Y1/Y3/Y6)
 *   - 10 edge cases (CMI-invalidité, personne morale, 3+ kids, VU/VU,
 *     high-power Tesla 15 CV, malus cap hit, etc.)
 */
import type { CalculationContext } from '@immatout/calc';

export type TestCase = {
  id: string;
  label: string;
  ctx: CalculationContext;
  notes?: string;
};

const REGISTRATION_DATE = '2026-04-23';

function fr(
  id: string,
  label: string,
  ctx: Partial<CalculationContext['vehicle']>,
  region: CalculationContext['region'],
  vehicleCase: CalculationContext['vehicleCase'],
  household: Partial<CalculationContext['household']> = {},
  notes?: string,
): TestCase {
  const base: TestCase = {
    id,
    label,
    ctx: {
      vehicle: {
        genre: 'VP',
        energy: 'ESSENCE',
        fiscalHorsepower: 5,
        firstRegistrationDate: '2026-01-01',
        co2WltpGPerKm: 120,
        massInRunningOrderKg: 1300,
        ...ctx,
      },
      vehicleCase,
      region,
      registrationDate: REGISTRATION_DATE,
      household: {
        dependentChildren: 0,
        hasDisabilityCard: false,
        isLegalEntity: false,
        ...household,
      },
    },
  };
  if (notes) base.notes = notes;
  return base;
}

